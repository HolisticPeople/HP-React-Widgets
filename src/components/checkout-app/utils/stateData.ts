// Map of country codes to their states/provinces
export const COUNTRY_STATES: Record<string, StateOption[]> = {
  US: US_STATES,
  CA: CA_PROVINCES,
  AU: AU_STATES,
  MX: MX_STATES,
  BR: BR_STATES,
};

/**
 * Check if a country has defined states/provinces
 */
export function countryHasStates(countryCode: string): boolean {
  return countryCode in COUNTRY_STATES;
}

/**
 * Get states for a country
 */
export function getStatesForCountry(countryCode: string): StateOption[] {
  return COUNTRY_STATES[countryCode] || [];
}

/**
 * Get state label based on country
 */
export function getStateLabel(countryCode: string): string {
  switch (countryCode) {
    case 'US':
      return 'State';
    case 'CA':
      return 'Province';
    case 'AU':
      return 'State/Territory';
    case 'MX':
    case 'BR':
      return 'State';
    default:
      return 'State/Province';
  }
}
